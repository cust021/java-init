package p01;

public class DataType {

	 public static void main(String[] args) {
		 //문자
		 char c = '1';
		 byte b = 1;
		 
		 int i = 1;
		 short s = (short)i;
		 
		 //정수
		 int i1 = 1000000000;
		 long l = 10000000000L;
		 
		 //실수
		 float f = 1.1F;
		 double d = 1.1;
		 
		 //논리
		 boolean bl = true;
	 }
}
