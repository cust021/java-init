package p03;

public class PrintUtil {

	public static void printArr(String[] arr) {
		for(int i=0;i<arr.length;i++) {
			System.out.println("arr[" + i + "] : " + arr[i]);
		}
	}

	public static void printArr(int[] arr) {
		for(int i=0;i<arr.length;i++) {
			System.out.println("arr[" + i + "] : " + arr[i]);
		}
	}
}